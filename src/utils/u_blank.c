#include "../../includes/cub3d.h"

int is_blank(const char *s)
{
	size_t i;

	if (!s)
		return 1;
	i = 0;
	while (s[i])
	{
		if (s[i] != ' ' && s[i] != '\t' && s[i] != '\n' &&
			s[i] != '\r' && s[i] != '\v' && s[i] != '\f')
			return 0;
		i++;
	}
	return 1;
}